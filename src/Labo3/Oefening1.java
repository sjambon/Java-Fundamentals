package Labo3;

/**
 * Dit programma vraagt een karakter, berekent de ASCII-nummer hiervan en
 * print deze op het scherm
 *
 * @author Steven Jambon
 * @version 8 oct 2018
 */

public class Oefening1 {
    public static void main(String[] args) {
        double var1 = 10.0;
        System.out.println("De double waarde is:" + var1);
        System.out.println("De float-waarde hiervan is: " + (float)var1);
        System.out.println("De long-waarde hiervan is: " + (long)var1);
        System.out.println("De int-waarde hiervan is: " + (int)var1);
        System.out.println("De short-waarde hiervan is: " + (short)var1);
        System.out.println("De char-waarde hiervan is: " + (char)var1);
        System.out.println("De byte-waarde hiervan is: " + (byte)var1);
    }
}
